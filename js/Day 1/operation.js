// Arithmetic Operators
let a = 10;
let b = 5;
console.log(a + b); // Output: 15
console.log(a - b); // Output: 5
console.log(a * b); // Output: 50
console.log(a / b); // Output: 2
console.log(a % b); // Output: 0
// Assignment Operators
let c = 10;
c += 5; // c = c + 5
console.log(c); // Output: 15
c -= 5; // c = c - 5
console.log(c); // Output: 10
c *= 5; // c = c * 5
console.log(c); // Output: 50
c /= 5; // c = c / 5
console.log(c); // Output: 10
c %= 5; // c = c % 5
console.log(c); // Output: 0
// Comparison Operators
let d = 10;
let e = 5;
console.log(d == e); // Output: false
console.log(d != e); // Output: true
console.log(d > e); // Output: true
console.log(d < e); // Output: false
console.log(d >= e); // Output: true
console.log(d <= e); // Output: false
// Logical Operators
let f = true;
let g = false;
console.log(f && g); // Output: false
console.log(f || g); // Output: true
console.log(!f); // Output: false  
console.log(!g); // Output: true
// Ternary Operator
let h = 10;
let i = 5;
let result = (h > i) ? "h is greater than i" : "h is not greater than i";
console.log(result); // Output: h is greater than i
