//Variables are used to store data in programming. They can hold different types of data such as numbers, strings, and booleans. In JavaScript, you can declare a variable using the var, let, or const keywords.

// Using var to declare a variable
var name = "Ali";
console.log(name); // Output: Ali

// Using let to declare a variable
let age = 25;
console.log(age); // Output: 25

// Using const to declare a variable
const PI = 3.14;
console.log(PI); // Output: 3.14